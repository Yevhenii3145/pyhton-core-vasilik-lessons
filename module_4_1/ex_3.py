# срезы
p = ["new", "new_1", "new_2", "new_3", "new_4"]
print(p[0:2])  # элемент с индексом 2 не войдет в срез
print(p[:5:1])  # [start:stop:step]
print(p[::])  # за замовчуванням крок 1
print(p[::-1])
print(p[::-2])
print(p[400:5000:1])  # нет ошибки а просто []
# print(p[5]) ошибка т.к. нет єлемента с таким индексом
# print(p[::-0]) ошибка т.к. шаг не может быть 0
print(p[-0])
print(p[-5])
